/*
 	 Stack(I): It is a child class of Vector. It is a specially designed class for LIFO(Last-In First Out) order.
 	 
 	 Constructor 
 	 	 Stack s = new Stack()
 	 	 
 	 Methods
 	     Object push(Object o) - to insert an Object on top of the stack
 	     Object pop()          - to remove an Object from top of the stack and return it
 	     Object peek()         - to return top of the stack without removal
 	     boolean empty()       - returns true if stack is empty
 	     int search(Object o)  - returns offset(position from top of stack(not 0 based)) if the element is available otherwise -1
 */
